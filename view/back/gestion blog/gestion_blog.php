    // Sorting functionality
    function compareRows(a, b, key, type) {
      let valA = a.querySelector(key).textContent.trim();
      let valB = b.querySelector(key).textContent.trim();
      if(type === 'number') {
        return parseInt(valA) - parseInt(valB);
      } else if(type === 'date') {
        return new Date(valA) - new Date(valB);
      } else {
        return valA.localeCompare(valB);
      }
    }
    document.addEventListener('DOMContentLoaded', function() {
      const searchInput = document.getElementById('blogSearchInput');
      if (searchInput) {
        searchInput.addEventListener('keyup', function() {
          const filter = searchInput.value.toLowerCase();
          const table = searchInput.closest('.card').querySelector('table');
          if (!table) return;
          const rows = table.getElementsByTagName('tr');
          for (let i = 1; i < rows.length; i++) { // skip header row
            const cells = rows[i].getElementsByTagName('td');
            let match = false;
            if (cells.length > 0) {
              // ID in first cell, Name in third, Description in fifth
              const id = cells[0].textContent.toLowerCase();
              const name = cells[2].textContent.toLowerCase();
              const desc = cells[4] ? cells[4].textContent.toLowerCase() : '';
              if (id.includes(filter) || name.includes(filter) || desc.includes(filter)) {
                match = true;
              }
            }
            rows[i].style.display = match ? '' : 'none';
          }
        });
      }
      // Sorting
      const sortSelect = document.getElementById('blogSortSelect');
      if (sortSelect) {
        sortSelect.addEventListener('change', function() {
          const sortBy = this.value;
          const tbody = document.querySelector('table tbody');
          const rows = Array.from(tbody.querySelectorAll('tr'));
          let key, type;
          if(sortBy === 'id') {
            key = 'td:nth-child(1)';
            type = 'number';
          } else if(sortBy === 'name') {
            key = 'td:nth-child(3)';
            type = 'string';
          } else if(sortBy === 'date') {
            key = 'td:nth-child(6)';
            type = 'date';
          }
          rows.sort((a, b) => compareRows(a, b, key, type));
          rows.forEach(row => tbody.appendChild(row));
        });
      }
